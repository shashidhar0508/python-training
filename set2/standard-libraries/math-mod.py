import math as m

# for square root of number
print("square root of 4 is : ",m.sqrt(4))

# for factorial of a number
print("factorial of 4 is : ",m.factorial(4))

# for ceil of number
print("ceil of 3.23 is : ",m.ceil(3.23))

# for floor of number
print("floor of 4.95 is : ",m.floor(4.95))

# for exp(x) of number
print("exp(4) is : ",m.exp(4))

# for pow(x, y)
print("pow(4, 2) is : ",m.pow(4, 2))

