# if statement
a,b=10,5
if a>b:
    print("a is greater than b")


# if-else statement
a,b=10,50
if a>b:
    print("a is greater than b")
else:
    print("a is less than b")


# if-elif-else statement
a=50
if a>0 and a<20:
    print("a is greater than 10 and less than 20")
elif a>20 and a<30:
    print("a is greater than 20 and less than 30")
elif a>30 and a<60:
    print("a is greater than 30 and less than 60")
else:
    print("a is greater than 60")